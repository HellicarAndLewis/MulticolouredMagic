namespace xmlgui {
	struct Event {
		enum EventType {
			TOUCH_MOVED,
			TOUCH_UP,
			TOUCH_DOWN
		};
		xmlgui::Control *control;
		
		EventType type;
		
		Event(Control *control, EventType type) {
			this->control = control;
			this->type = type;
		}
	};
}